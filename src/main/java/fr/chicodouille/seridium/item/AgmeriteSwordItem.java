@SeridiumodModElements.ModElement.Tag
public class AgmeriteSwordItem extends SeridiumodModElements.ModElement {
	@ObjectHolder("seridiumod:agmerite_sword")
	public static final Item block = null;

	public AgmeriteSwordItem(SeridiumodModElements instance) {
		super(instance, 37);
	}

	@Override
	public void initElements() {
		elements.items.add(() -> new SwordItem(new IItemTier() {
			public int getMaxUses() {
				return 2500;
			}

			public float getEfficiency() {
				return 11f;
			}

			public float getAttackDamage() {
				return 7f;
			}

			public int getHarvestLevel() {
				return 5;
			}

			public int getEnchantability() {
				return 20;
			}

			public Ingredient getRepairMaterial() {
				return Ingredient.fromStacks(new ItemStack(AgmeriteIngotItem.block));
			}
		}, 3, -2.2f, new Item.Properties().group(SeridiumItemGroup.tab)) {
		}.setRegistryName("agmerite_sword"));
	}
}
